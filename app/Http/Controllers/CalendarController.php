            3 => 'March',
            4 => 'April',
            5 => 'May',
            6 => 'June',
            7 => 'July',
            8 => 'August',
            9 => 'September',
            10 => 'October',
            11 => 'November',
            12 => 'December'
        );
        $matchedMonth = null;

        foreach ($monthNames as $key => $value) {
            if ($key == $month) {
                $matchedMonth = $value;
                break;
            }
        }

        return view('calendar.view', [
            'year' => $year,
            'month' => $month
        ]);
    }
